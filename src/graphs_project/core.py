def soma_lista(numeros):
    """
    Soma todos os números em uma lista.
    
    Args:
        numeros (list): Lista de números para somar
        
    Returns:
        float: A soma de todos os números
        
    Exemplo:
        >>> soma_lista([1, 2, 3, 4])
        10
    """
    return sum(numeros)